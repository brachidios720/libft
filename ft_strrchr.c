#include "libft.h"

char	*ft_strrchr(const char *str, int c)
{
	int	i;
	int	i2;

	i2 = 0;
	while (str[i2])
		i2++;
	i = i2;
	while (i >= 0)
	{
		if (str[i] == (char)c)
			return ((char *)str + i);
		i--;
	}
	return (NULL);
}
